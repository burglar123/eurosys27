def load_workload(path: str, limit: Optional[int] = None) -> List[Dict[str, Any]]:
    if not os.path.exists(path):
        raise FileNotFoundError(f"Workload file not found: {path}")

    workload: List[Dict[str, Any]] = []

    with open(path, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            if limit is not None and len(workload) >= limit:
                break

            line = line.strip()
            if not line:
                continue

            try:
                req = json.loads(line)
            except json.JSONDecodeError as exc:
                raise ValueError(f"Invalid JSON at {path}:{line_no}: {exc}") from exc

            validate_request(req, line_no)
            workload.append(req)

    if not workload:
        raise ValueError(f"No requests loaded from {path}")

    return workload


def validate_request(req: Dict[str, Any], line_no: int) -> None:
    required = ["request_id", "slo_tpot_ms", "slo_class"]
    for key in required:
        if key not in req:
            raise ValueError(f"Missing key '{key}' in workload line {line_no}")

    if "prompt" not in req and "input_ids" not in req:
        raise ValueError(
            f"Workload line {line_no} must contain either 'prompt' or 'input_ids'"
        )

    if "arrival_offset_sec" not in req and "arrival_ts" not in req:
        raise ValueError(
            f"Workload line {line_no} must contain either "
            f"'arrival_offset_sec' or 'arrival_ts'"
        )


def load_workload_meta(workload_path: str) -> Dict[str, Any]:
    meta_path = workload_path + ".meta.json"
    if not os.path.exists(meta_path):
        return {}

    with open(meta_path, "r", encoding="utf-8") as f:
        return json.load(f)


def make_pearl_config(args: argparse.Namespace) -> PEARLConfig:
    """
    Compatible with both styles:

    Newer style:
      PEARLConfig(
          draft_model_path=...,
          target_model_path=...,
          gamma=...,
          ...
      )

    Older style:
      PEARLConfig(
          draft_model,
          target_model,
          draft_tensor_parallel_size=...,
          target_tensor_parallel_size=...,
          ...
      )
    """
    common_kwargs = {
        "draft_tensor_parallel_size": args.draft_tp,
        "target_tensor_parallel_size": args.target_tp,
        "gpu_memory_utilization": args.gpu_memory_utilization,
    }

    # Try new named-path style with gamma.
    try:
        return PEARLConfig(
            draft_model_path=args.draft_model,
            target_model_path=args.target_model,
            gamma=args.gamma,
            **common_kwargs,
        )
    except TypeError:
        pass

    # Try old positional style with gamma.
    try:
        return PEARLConfig(
            args.draft_model,
            args.target_model,
            gamma=args.gamma,
            **common_kwargs,
        )
    except TypeError:
        pass

    # Fallback: old positional style without gamma.
    return PEARLConfig(
        args.draft_model,
        args.target_model,
        **common_kwargs,
    )


def make_sampling_params(req: Dict[str, Any], args: argparse.Namespace) -> SamplingParams:
    temperature = float(req.get("temperature", args.temperature))
    max_tokens = int(req.get("max_tokens", args.max_tokens))
    ignore_eos = bool(req.get("ignore_eos", args.ignore_eos))

    return SamplingParams(
        temperature=temperature,
        max_tokens=max_tokens,
        ignore_eos=ignore_eos,
    )


def get_request_prompt(req: Dict[str, Any]) -> Any:
    if "input_ids" in req:
        return req["input_ids"]
    return req["prompt"]


def absolute_arrival_ts(
    req: Dict[str, Any],
    workload_start_ts: float,
    use_absolute_arrival_ts: bool,
) -> float:
    if use_absolute_arrival_ts and "arrival_ts" in req:
        return float(req["arrival_ts"])

    if "arrival_offset_sec" in req:
        return workload_start_ts + float(req["arrival_offset_sec"])

    return float(req["arrival_ts"])


def add_request_with_metadata(
    engine: PEARLEngine,
    prompt: Any,
    sampling_params: SamplingParams,
    req: Dict[str, Any],
    arrival_ts: float,
    fallback_without_metadata: bool = True,
) -> None:
    """
    Prefer the extended add_request signature.

    If the local repo does not yet support metadata, optionally fallback to
    add_request(prompt, sampling_params). Metrics can still be computed by
    output-order fallback, but engine traces will not contain request metadata.
    """
    try:
        engine.add_request(
            prompt,
            copy.deepcopy(sampling_params),
            request_id=req["request_id"],
            arrival_ts=arrival_ts,
            slo_tpot_ms=float(req["slo_tpot_ms"]),
            slo_class=req["slo_class"],
            per_request_gamma=int(req.get("per_request_gamma", 0)),
        )
    except TypeError:
        if not fallback_without_metadata:
            raise

        print(
            "[WARN] engine.add_request(...) does not accept metadata. "
            "Retrying without metadata. Trace alignment may rely on output order."
        )
        engine.add_request(prompt, copy.deepcopy(sampling_params))


def try_clear_traces(engine: PEARLEngine) -> None:
    if hasattr(engine, "clear_traces"):
        try:
            engine.clear_traces()
        except Exception as exc:
            print(f"[WARN] engine.clear_traces() failed: {exc}")


def try_get_traces(engine: PEARLEngine) -> Any:
    if not hasattr(engine, "get_traces"):
        print("[WARN] engine.get_traces() not found. Will synthesize minimal traces.")
        return []

    try:
        return engine.get_traces()
    except Exception as exc:
        print(f"[WARN] engine.get_traces() failed: {exc}")
        return []


def extract_trace_list(trace_payload: Any) -> List[Dict[str, Any]]:
    """
    Accept several trace payload shapes:

    1. [ {...}, {...} ]

    2. {
         "traces": [ ... ]
       }

    3. {
         "requests": [ ... ]
       }

    4. {
         "sequences": [ ... ]
       }
    """
    if trace_payload is None:
        return []

    if isinstance(trace_payload, list):
        return [x for x in trace_payload if isinstance(x, dict)]

    if isinstance(trace_payload, dict):
        for key in ("traces", "requests", "sequences", "request_traces"):
            value = trace_payload.get(key)
            if isinstance(value, list):
                return [x for x in value if isinstance(x, dict)]

    return []
def select_request_level_traces(
    trace_rows: List[Dict[str, Any]],
    abs_workload: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """
    engine.get_traces() may return low-level event traces instead of one row per request.
    We only keep request-level traces.

    Rules:
    1. If rows contain request_id matching workload request_id, keep those rows.
    2. If trace row count equals workload size, trust positional alignment.
    3. Otherwise discard engine traces and let merge_traces_with_workload synthesize
       request-level traces from workload + engine outputs.
    """
    if not trace_rows:
        return []

    workload_ids = {str(req["request_id"]) for req in abs_workload}

    matched = []
    for row in trace_rows:
        req_id = first_present(
            row,
            [
                "request_id",
                "external_request_id",
                "req_id",
                "id",
            ],
        )
        if req_id is not None and str(req_id) in workload_ids:
            matched.append(row)

    if matched:
        if len(matched) != len(abs_workload):
            print(
                f"[WARN] Matched {len(matched)} request-level traces, "
                f"but workload chunk has {len(abs_workload)} requests."
            )
        return matched

    if len(trace_rows) == len(abs_workload):
        return trace_rows

    print(
        f"[WARN] Discard engine traces: got {len(trace_rows)} trace rows "
        f"for {len(abs_workload)} workload requests. "
        f"These are likely low-level event traces, not request-level traces."
    )
    return []


def first_present(row: Dict[str, Any], keys: List[str]) -> Any:
    for key in keys:
        if key in row and row[key] is not None:
            return row[key]
    return None


def to_float(value: Any, default: Optional[float] = None) -> Optional[float]:
    if value is None:
        return default
    try:
        return float(value)
    except Exception:
        return default


def to_int(value: Any, default: Optional[int] = None) -> Optional[int]:
    if value is None:
        return default
    try:
        return int(value)
    except Exception:
        return default


def infer_num_output_tokens(
    row: Dict[str, Any],
    fallback: Optional[int] = None,
) -> int:
    value = first_present(
        row,
        [
            "num_output_tokens",
            "num_completion_tokens",
            "completion_tokens",
            "output_tokens",
            "num_generated_tokens",
            "num_tokens",
            "generated_tokens",
        ],
    )

    if isinstance(value, int):
        return value

    if isinstance(value, float):
        return int(value)

    if isinstance(value, list):
        return len(value)

    value_int = to_int(value)
    if value_int is not None:
        return value_int

    token_ids = first_present(row, ["token_ids", "output_token_ids", "completion_token_ids"])
    if isinstance(token_ids, list):
        return len(token_ids)

    if fallback is not None:
        return int(fallback)

    return 0


def infer_decode_elapsed_ms(
    row: Dict[str, Any],
    num_output_tokens: int,
    fallback_engine_elapsed_s: Optional[float] = None,
) -> Optional[float]:
    direct_ms = first_present(
        row,
        [
            "decode_elapsed_ms",
            "decoding_elapsed_ms",
            "generation_elapsed_ms",
            "elapsed_ms",
        ],
    )
    direct_ms = to_float(direct_ms)
    if direct_ms is not None:
        return direct_ms

    direct_s = first_present(
        row,
        [
            "decode_elapsed_s",
            "decoding_elapsed_s",
            "generation_elapsed_s",
            "elapsed_s",
            "elapsed_time",
        ],
    )
    direct_s = to_float(direct_s)
    if direct_s is not None:
        return direct_s * 1000.0

    finish_ts = to_float(
        first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"])
    )
    decode_start_ts = to_float(
        first_present(
            row,
            [
                "decode_start_ts",
                "decoding_start_ts",
                "first_decode_ts",
                "first_token_ts",
                "start_decode_ts",
            ],
        )
    )

    if finish_ts is not None and decode_start_ts is not None and finish_ts >= decode_start_ts:
        return (finish_ts - decode_start_ts) * 1000.0

    start_ts = to_float(first_present(row, ["start_ts", "start_time", "arrival_ts"]))
    if finish_ts is not None and start_ts is not None and finish_ts >= start_ts:
        return (finish_ts - start_ts) * 1000.0

    if fallback_engine_elapsed_s is not None and num_output_tokens > 0:
        # Last-resort fallback. This is only a rough approximation for old traces.
        return fallback_engine_elapsed_s * 1000.0

    return None


def infer_observed_tpot_ms(
    row: Dict[str, Any],
    num_output_tokens: int,
    fallback_engine_elapsed_s: Optional[float] = None,
) -> Optional[float]:
    direct_tpot = first_present(
        row,
        [
            "observed_tpot_ms",
            "tpot_ms",
            "avg_tpot_ms",
            "mean_tpot_ms",
        ],
    )
    direct_tpot = to_float(direct_tpot)
    if direct_tpot is not None:
        return direct_tpot

    if num_output_tokens <= 0:
        return None

    decode_elapsed_ms = infer_decode_elapsed_ms(
        row,
        num_output_tokens=num_output_tokens,
        fallback_engine_elapsed_s=fallback_engine_elapsed_s,
    )

    if decode_elapsed_ms is None:
        return None

    return decode_elapsed_ms / max(num_output_tokens, 1)


def build_abs_workload(
    workload: List[Dict[str, Any]],
    workload_start_ts: float,
    use_absolute_arrival_ts: bool,
) -> List[Dict[str, Any]]:
    abs_workload: List[Dict[str, Any]] = []

    for req in workload:
        row = dict(req)
        row["arrival_ts"] = absolute_arrival_ts(
            req,
            workload_start_ts=workload_start_ts,
            use_absolute_arrival_ts=use_absolute_arrival_ts,
        )
        abs_workload.append(row)

    return abs_workload


def merge_traces_with_workload(
    trace_rows: List[Dict[str, Any]],
    abs_workload: List[Dict[str, Any]],
    output_num_tokens: List[int],
    output_num_acc_tokens: Any,
    run_start_ts: float,
    run_end_ts: float,
    engine_elapsed_s: float,
) -> List[Dict[str, Any]]:
    """
    Merge engine traces with workload metadata.

    Priority:
    1. Match by request_id.
    2. Fallback to output order / workload order.
    3. If no engine traces, synthesize minimal trace rows.
    """
    workload_by_id = {
        str(req["request_id"]): req
        for req in abs_workload
        if "request_id" in req
    }

    merged: List[Dict[str, Any]] = []

    if trace_rows:
        for idx, trace in enumerate(trace_rows):
            req_id = first_present(
                trace,
                [
                    "request_id",
                    "external_request_id",
                    "req_id",
                    "id",
                ],
            )

            base: Dict[str, Any] = {}

            if req_id is not None and str(req_id) in workload_by_id:
                base.update(workload_by_id[str(req_id)])
            elif idx < len(abs_workload):
                base.update(abs_workload[idx])

            base.update(trace)

            if "request_id" not in base and idx < len(abs_workload):
                base["request_id"] = abs_workload[idx]["request_id"]

            if "arrival_ts" not in base and idx < len(abs_workload):
                base["arrival_ts"] = abs_workload[idx]["arrival_ts"]

            if "num_output_tokens" not in base and idx < len(output_num_tokens):
                base["num_output_tokens"] = output_num_tokens[idx]

            if "num_acc_tokens" not in base:
                try:
                    base["num_acc_tokens"] = output_num_acc_tokens[idx]
                except Exception:
                    pass

            merged.append(base)

        return merged

    # No engine traces: synthesize minimal traces from outputs.
    for idx, req in enumerate(abs_workload):
        row = dict(req)

        if idx < len(output_num_tokens):
            row["num_output_tokens"] = int(output_num_tokens[idx])
        else:
            row["num_output_tokens"] = 0

        try:
            row["num_acc_tokens"] = output_num_acc_tokens[idx]
        except Exception:
            pass

        row["run_start_ts"] = run_start_ts
        row["finish_ts"] = run_end_ts
        row["decode_elapsed_ms"] = engine_elapsed_s * 1000.0

        merged.append(row)

    return merged


def choose_goodput_denominator_s(
    rows: List[Dict[str, Any]],
    engine_elapsed_s: float,
    mode: str,
    workload_meta: Dict[str, Any],
) -> float:
    if mode == "engine_elapsed":
        return max(engine_elapsed_s, 1e-9)

    if mode == "workload_duration":
        duration = to_float(workload_meta.get("duration_sec"))
        if duration is not None and duration > 0:
            return duration

        offsets = [
            to_float(row.get("arrival_offset_sec"))
            for row in rows
            if to_float(row.get("arrival_offset_sec")) is not None
        ]
        if offsets:
            return max(max(offsets) - min(offsets), 1e-9)

        return max(engine_elapsed_s, 1e-9)

    if mode == "trace_makespan":
        starts = [
            to_float(row.get("arrival_ts"))
            for row in rows
            if to_float(row.get("arrival_ts")) is not None
        ]
        finishes = [
            to_float(first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"]))
            for row in rows
        ]
        finishes = [x for x in finishes if x is not None]

        if starts and finishes:
            makespan = max(finishes) - min(starts)
            if makespan > 0:
                return makespan

        return max(engine_elapsed_s, 1e-9)

    raise ValueError(f"Unknown goodput denominator mode: {mode}")


def summarize_group(
    rows: List[Dict[str, Any]],
    denominator_s: float,
) -> Dict[str, Any]:
    if not rows:
        return {
            "num_requests": 0,
            "num_attained": 0,
            "slo_attainment": 0.0,
            "goodput_tokens_per_s": 0.0,
            "mean_tpot_ms": None,
        }

    attained_rows = [row for row in rows if row.get("slo_attained") is True]
    good_tokens = sum(int(row.get("num_output_tokens", 0)) for row in attained_rows)
    tpot_values = [
        float(row["observed_tpot_ms"])
        for row in rows
        if row.get("observed_tpot_ms") is not None
    ]

    return {
        "num_requests": len(rows),
        "num_attained": len(attained_rows),
        "slo_attainment": len(attained_rows) / max(len(rows), 1),
        "goodput_tokens_per_s": good_tokens / max(denominator_s, 1e-9),
        "mean_tpot_ms": sum(tpot_values) / len(tpot_values) if tpot_values else None,
        "total_output_tokens": sum(int(row.get("num_output_tokens", 0)) for row in rows),
        "attained_output_tokens": good_tokens,
    }


def compute_metrics(
    rows: List[Dict[str, Any]],
    engine_elapsed_s: float,
    denominator_mode: str,
    workload_meta: Dict[str, Any],
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    evaluated_rows: List[Dict[str, Any]] = []

    for row in rows:
        row = dict(row)

        num_output_tokens = infer_num_output_tokens(row)
        row["num_output_tokens"] = int(num_output_tokens)

        observed_tpot_ms = infer_observed_tpot_ms(
            row,
            num_output_tokens=num_output_tokens,
            fallback_engine_elapsed_s=engine_elapsed_s,
        )
        row["observed_tpot_ms"] = observed_tpot_ms

        slo_tpot_ms = to_float(row.get("slo_tpot_ms"))
        row["slo_tpot_ms"] = slo_tpot_ms

        if observed_tpot_ms is None or slo_tpot_ms is None or num_output_tokens <= 0:
            row["slo_attained"] = False
        else:
            row["slo_attained"] = observed_tpot_ms <= slo_tpot_ms

        evaluated_rows.append(row)

    denominator_s = choose_goodput_denominator_s(
        evaluated_rows,
        engine_elapsed_s=engine_elapsed_s,
        mode=denominator_mode,
        workload_meta=workload_meta,
    )

    overall = summarize_group(evaluated_rows, denominator_s)

    by_category: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    by_slo_class: Dict[str, List[Dict[str, Any]]] = defaultdict(list)

    for row in evaluated_rows:
        by_category[str(row.get("category", "unknown"))].append(row)
        by_slo_class[str(row.get("slo_class", "unknown"))].append(row)

    metrics = {
        "overall": overall,
        "by_category": {
            key: summarize_group(value, denominator_s)
            for key, value in sorted(by_category.items())
        },
        "by_slo_class": {
            key: summarize_group(value, denominator_s)
            for key, value in sorted(by_slo_class.items())
        },
        "engine_elapsed_s": engine_elapsed_s,
        "goodput_denominator_mode": denominator_mode,
        "goodput_denominator_s": denominator_s,
    }

    return metrics, evaluated_rows


def run_warmup(engine: PEARLEngine, args: argparse.Namespace) -> None:
    if args.warmup_iters <= 0:
        return

    print(f"[INFO] Running warmup: {args.warmup_iters} iteration(s)")

    warmup_params = SamplingParams(
        temperature=0.0,
        max_tokens=args.warmup_max_tokens,
        ignore_eos=False,
    )

    for i in range(args.warmup_iters):
        engine.add_request(f"Warmup request {i}.", copy.deepcopy(warmup_params))
        engine.generate()

    try_clear_traces(engine)
    print("[INFO] Warmup finished")


def maybe_dump_engine_trace(engine: PEARLEngine, path: Optional[str]) -> None:
    if not path:
        return

    if not hasattr(engine, "dump_traces_json"):
        print("[WARN] engine.dump_traces_json() not found; skip engine trace dump")
        return

    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)

    try:
        engine.dump_traces_json(path)
        print(f"[OK] Dumped engine traces to: {path}")
    except Exception as exc:
        print(f"[WARN] engine.dump_traces_json({path}) failed: {exc}")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Evaluate saved multi-SLO workload on nano-PEARL."
    )

    # Model paths.
    parser.add_argument("--draft-model", "-d", type=str, required=True)
    parser.add_argument("--target-model", "-t", type=str, required=True)

    # Model/runtime config.
    parser.add_argument("--draft-tp", type=int, default=1)
    parser.add_argument("--target-tp", type=int, default=2)
    parser.add_argument("--gpu-memory-utilization", type=float, default=0.9)
    parser.add_argument("--gamma", type=int, default=4)

    # Generation defaults.
    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--max-tokens", type=int, default=256)
    parser.add_argument("--ignore-eos", action="store_true")

    # Workload.
    parser.add_argument("--workload-in", type=str, required=True)
    parser.add_argument("--limit-requests", type=int, default=None)
    parser.add_argument(
        "--use-workload-absolute-arrival-ts",
        action="store_true",
        help=(
            "Use arrival_ts in workload directly. By default, arrival_offset_sec is "
            "converted to current wall-clock time."
        ),
    )
    parser.add_argument(
        "--replay-arrivals",
        action="store_true",
        help=(
            "Sleep according to arrival offsets before adding requests. "
            "This is useful only if your engine has a background serving loop. "
            "For current add-all-then-generate mode, keep this disabled."
        ),
    )

    # Warmup.
    parser.add_argument("--warmup-iters", type=int, default=0)
    parser.add_argument("--warmup-max-tokens", type=int, default=32)

    # Metric options.
    parser.add_argument(
        "--goodput-denominator",
        choices=["engine_elapsed", "trace_makespan", "workload_duration"],
        default="engine_elapsed",
        help=(
            "engine_elapsed is recommended for the current offline add-all-then-generate "
            "benchmark. trace_makespan is suitable for a real online serving loop."
        ),
    )

    # Output.
    parser.add_argument("--out", type=str, required=True)
    parser.add_argument(
        "--engine-trace-out",
        type=str,
        default=None,
        help="Optional raw engine trace JSON path via engine.dump_traces_json().",
    )
    parser.add_argument(
        "--save-output-text",
        action="store_true",
        help="Save generated output text into result JSON. This can make files large.",
    )
    parser.add_argument(
        "--fail-if-add-request-metadata-unsupported",
        action="store_true",
        help="Raise error if engine.add_request does not support metadata.",
    )

    args = parser.parse_args()

    workload = load_workload(args.workload_in, limit=args.limit_requests)
    workload_meta = load_workload_meta(args.workload_in)

    print(f"[INFO] Loaded workload: {args.workload_in}")
    print(f"[INFO] Number of requests: {len(workload)}")

    config = make_pearl_config(args)
    engine = PEARLEngine(config)

    output_text: List[str] = []
    num_tokens: List[int] = []
    num_acc_tokens: Any = []
    elapsed_time = 0.0
    trace_payload: Any = None
    evaluated_rows: List[Dict[str, Any]] = []
    metrics: Dict[str, Any] = {}

    try:
        run_warmup(engine, args)

        workload_start_ts = time.time()
        abs_workload = build_abs_workload(
            workload,
            workload_start_ts=workload_start_ts,
            use_absolute_arrival_ts=args.use_workload_absolute_arrival_ts,
        )

        print("[INFO] Adding requests to engine...")

        first_arrival_offset = to_float(abs_workload[0].get("arrival_offset_sec"), 0.0) or 0.0
        replay_wall_start = time.time()

        for idx, req in enumerate(abs_workload):
            if args.replay_arrivals:
                offset = to_float(req.get("arrival_offset_sec"), 0.0) or 0.0
                target_delay = max(offset - first_arrival_offset, 0.0)
                now_delay = time.time() - replay_wall_start
                sleep_s = target_delay - now_delay
                if sleep_s > 0:
                    time.sleep(sleep_s)

            sampling_params = make_sampling_params(req, args)
            prompt = get_request_prompt(req)

            add_request_with_metadata(
                engine=engine,
                prompt=prompt,
                sampling_params=sampling_params,
                req=req,
                arrival_ts=float(req["arrival_ts"]),
                fallback_without_metadata=not args.fail_if_add_request_metadata_unsupported,
            )

            if (idx + 1) % 100 == 0:
                print(f"[INFO] Added {idx + 1}/{len(abs_workload)} requests")

        print("[INFO] Starting generation...")
        run_start_ts = time.time()
        output_text, num_tokens, num_acc_tokens, elapsed_time = engine.generate()
        run_end_ts = time.time()
        print(f"[OK] Generation finished. engine_elapsed_s={elapsed_time:.6f}")

        trace_payload = try_get_traces(engine)
        trace_rows = extract_trace_list(trace_payload)
        trace_rows = select_request_level_traces(trace_rows, abs_workload)

        maybe_dump_engine_trace(engine, args.engine_trace_out)

        merged_rows = merge_traces_with_workload(
            trace_rows=trace_rows,
            abs_workload=abs_workload,
            output_num_tokens=num_tokens,
            output_num_acc_tokens=num_acc_tokens,
            run_start_ts=run_start_ts,
            run_end_ts=run_end_ts,
            engine_elapsed_s=elapsed_time,
        )

        metrics, evaluated_rows = compute_metrics(
            rows=merged_rows,
            engine_elapsed_s=elapsed_time,
            denominator_mode=args.goodput_denominator,
            workload_meta=workload_meta,
        )

        result: Dict[str, Any] = {
            "args": vars(args),
            "workload_meta": workload_meta,
            "metrics": metrics,
            "num_tokens": num_tokens,
            "num_acc_tokens": num_acc_tokens,
            "traces": evaluated_rows,
        }

        if args.save_output_text:
            result["output_text"] = output_text

        os.makedirs(os.path.dirname(args.out) or ".", exist_ok=True)
        with open(args.out, "w", encoding="utf-8") as f:
            json.dump(result, f, indent=2, ensure_ascii=False)

        print(f"[OK] Saved result: {args.out}")
        print(json.dumps(metrics["overall"], indent=2, ensure_ascii=False))
        print("[INFO] Breakdown by SLO class:")
        print(json.dumps(metrics["by_slo_class"], indent=2, ensure_ascii=False))
        print("[INFO] Breakdown by category:")
        print(json.dumps(metrics["by_category"], indent=2, ensure_ascii=False))

    finally:
        try:
            atexit.unregister(engine.exit)
        except Exception:
            pass

        try:
            engine.exit()
        except Exception as exc:
            print(f"[WARN] engine.exit() failed or was already called: {exc}")


if __name__ == "__main__":
    main()