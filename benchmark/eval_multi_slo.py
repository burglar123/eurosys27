def load_workload(path: str, limit: Optional[int] = None) -> List[Dict[str, Any]]:
    if not os.path.exists(path):
        raise FileNotFoundError(f"Workload file not found: {path}")

    workload: List[Dict[str, Any]] = []

    with open(path, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            if limit is not None and len(workload) >= limit:
                break

            line = line.strip()
            if not line:
                continue

            try:
                req = json.loads(line)
            except json.JSONDecodeError as exc:
                raise ValueError(f"Invalid JSON at {path}:{line_no}: {exc}") from exc

            validate_request(req, line_no)
            workload.append(req)

    if not workload:
        raise ValueError(f"No requests loaded from {path}")

    return workload


def validate_request(req: Dict[str, Any], line_no: int) -> None:
    required = ["request_id", "slo_tpot_ms", "slo_class"]
    for key in required:
        if key not in req:
            raise ValueError(f"Missing key '{key}' in workload line {line_no}")

    if "prompt" not in req and "input_ids" not in req:
        raise ValueError(
            f"Workload line {line_no} must contain either 'prompt' or 'input_ids'"
        )

    if "arrival_offset_sec" not in req and "arrival_ts" not in req:
        raise ValueError(
            f"Workload line {line_no} must contain either "
            f"'arrival_offset_sec' or 'arrival_ts'"
        )


def load_workload_meta(workload_path: str) -> Dict[str, Any]:
    meta_path = workload_path + ".meta.json"
    if not os.path.exists(meta_path):
        return {}

    with open(meta_path, "r", encoding="utf-8") as f:
        return json.load(f)


def make_pearl_config(args: argparse.Namespace) -> PEARLConfig:
    """
    Compatible with both styles:

    Newer style:
      PEARLConfig(
          draft_model_path=...,
          target_model_path=...,
          gamma=...,
          ...
      )

    Older style:
      PEARLConfig(
          draft_model,
          target_model,
          draft_tensor_parallel_size=...,
          target_tensor_parallel_size=...,
          ...
      )
    """
    common_kwargs = {
        "draft_tensor_parallel_size": args.draft_tp,
        "target_tensor_parallel_size": args.target_tp,
        "gpu_memory_utilization": args.gpu_memory_utilization,
        "execution_mode": args.execution_mode,
    }

    # Try new named-path style with gamma.
    try:
        return PEARLConfig(
            draft_model_path=args.draft_model,
            target_model_path=args.target_model,
            gamma=args.gamma,
            **common_kwargs,
        )
    except TypeError:
        pass

    # Older PEARLConfig variants may not expose execution_mode yet.
    common_kwargs.pop("execution_mode", None)

    try:
        return PEARLConfig(
            draft_model_path=args.draft_model,
            target_model_path=args.target_model,
            gamma=args.gamma,
            **common_kwargs,
        )
    except TypeError:
        pass

    # Try old positional style with gamma.
    try:
        return PEARLConfig(
            args.draft_model,
            args.target_model,
            gamma=args.gamma,
            **common_kwargs,
        )
    except TypeError:
        pass

    # Fallback: old positional style without gamma.
    return PEARLConfig(
        args.draft_model,
        args.target_model,
        **common_kwargs,
    )


def make_sampling_params(req: Dict[str, Any], args: argparse.Namespace) -> SamplingParams:
    temperature = float(req.get("temperature", args.temperature))
    max_tokens = int(req.get("max_tokens", args.max_tokens))
    ignore_eos = bool(req.get("ignore_eos", args.ignore_eos))

    return SamplingParams(
        temperature=temperature,
        max_tokens=max_tokens,
        ignore_eos=ignore_eos,
    )


def get_request_prompt(req: Dict[str, Any]) -> Any:
    if "input_ids" in req:
        return req["input_ids"]
    return req["prompt"]


def materialize_arrival_ts(
    req: Dict[str, Any],
    workload_start_ts: float,
    use_absolute_arrival_ts: bool,
    replay_arrivals: bool,
) -> float:
    """Return the wall-clock arrival timestamp to pass to the engine.

    ``arrival_offset_sec`` is workload metadata. It becomes a wall-clock
    timestamp only when we are replaying arrivals. In offline/chunked mode all
    requests are prepared immediately, so the evaluator records the real time at
    which the request is materialized instead of adding the offset to a synthetic
    workload start.
    """
    if use_absolute_arrival_ts and "arrival_ts" in req:
        return float(req["arrival_ts"])

    if replay_arrivals and "arrival_offset_sec" in req:
        return workload_start_ts + float(req["arrival_offset_sec"])

    if "arrival_offset_sec" not in req and "arrival_ts" in req:
        return float(req["arrival_ts"])

    return time.time()


def add_request_with_metadata(
    engine: PEARLEngine,
    prompt: Any,
    sampling_params: SamplingParams,
    req: Dict[str, Any],
    arrival_ts: float,
    fallback_without_metadata: bool = True,
) -> None:
    """
    Prefer the extended add_request signature.

    If the local repo does not yet support metadata, optionally fallback to
    add_request(prompt, sampling_params). Metrics can still be computed by
    output-order fallback, but engine traces will not contain request metadata.
    """
    try:
        engine.add_request(
            prompt,
            copy.deepcopy(sampling_params),
            request_id=req["request_id"],
            arrival_ts=arrival_ts,
            slo_tpot_ms=float(req["slo_tpot_ms"]),
            slo_class=req["slo_class"],
            per_request_gamma=int(req.get("per_request_gamma", 0)),
        )
    except TypeError:
        if not fallback_without_metadata:
            raise

        print(
            "[WARN] engine.add_request(...) does not accept metadata. "
            "Retrying without metadata. Trace alignment may rely on output order."
        )
        engine.add_request(prompt, copy.deepcopy(sampling_params))


def try_clear_traces(engine: PEARLEngine) -> None:
    if hasattr(engine, "clear_traces"):
        try:
            engine.clear_traces()
        except Exception as exc:
            print(f"[WARN] engine.clear_traces() failed: {exc}")


def try_get_traces(engine: PEARLEngine) -> Any:
    if not hasattr(engine, "get_traces"):
        print("[WARN] engine.get_traces() not found. Will synthesize minimal traces.")
        return []

    try:
        return engine.get_traces()
    except Exception as exc:
        print(f"[WARN] engine.get_traces() failed: {exc}")
        return []


def extract_trace_list(trace_payload: Any) -> List[Dict[str, Any]]:
    """
    Accept several trace payload shapes:

    1. [ {...}, {...} ]

    2. {
         "traces": [ ... ]
       }

    3. {
         "requests": [ ... ]
       }

    4. {
         "sequences": [ ... ]
       }
    """
    if trace_payload is None:
        return []

    if isinstance(trace_payload, list):
        return [x for x in trace_payload if isinstance(x, dict)]

    if isinstance(trace_payload, dict):
        for key in ("requests", "request_traces", "sequences", "traces"):
            value = trace_payload.get(key)
            if isinstance(value, list):
                return [x for x in value if isinstance(x, dict)]

    return []


REQUEST_ID_KEYS = ["request_id", "external_request_id", "req_id", "id"]
SEQ_ID_KEYS = ["seq_id", "sequence_id"]


def row_request_or_seq_id(row: Dict[str, Any]) -> Any:
    return first_present(row, REQUEST_ID_KEYS + SEQ_ID_KEYS)


def is_request_level_trace_row(
    row: Dict[str, Any],
    workload_ids: set[str],
    seq_ids: set[str],
) -> bool:
    req_id = first_present(row, REQUEST_ID_KEYS)
    seq_id = first_present(row, SEQ_ID_KEYS)
    has_known_id = (req_id is not None and str(req_id) in workload_ids) or (
        seq_id is not None and str(seq_id) in seq_ids
    )
    if not has_known_id:
        return False

    # Low-level scheduler traces usually describe a batch/iteration rather than
    # one request and carry vector fields such as request_ids/scheduled_seq_ids.
    if any(isinstance(row.get(key), list) for key in ("request_ids", "scheduled_seq_ids")):
        return False

    request_level_markers = [
        "finish_ts",
        "finished_ts",
        "decode_elapsed_ms",
        "observed_tpot_ms",
        "num_output_tokens",
        "num_decode_output_tokens",
    ]
    return any(key in row for key in request_level_markers)


def extract_high_level_trace_list(trace_payload: Any) -> List[Dict[str, Any]]:
    """Prefer one-row-per-request payload sections when the engine provides them."""
    if not isinstance(trace_payload, dict):
        return []

    for key in ("requests", "request_traces", "request_summaries", "sequences"):
        value = trace_payload.get(key)
        if isinstance(value, list):
            rows = [x for x in value if isinstance(x, dict)]
            if rows:
                print(f"[INFO] Using request-level trace payload key: {key}")
                return rows
    return []


def iter_low_level_trace_rows(trace_payload: Any) -> Iterable[Dict[str, Any]]:
    if isinstance(trace_payload, list):
        for row in trace_payload:
            if isinstance(row, dict):
                yield row
        return

    if isinstance(trace_payload, dict):
        for key in ("traces", "events", "iterations", "batches"):
            value = trace_payload.get(key)
            if isinstance(value, list):
                for row in value:
                    if isinstance(row, dict):
                        yield row


def value_for_member(value: Any, idx: int, member: Any) -> Any:
    if isinstance(value, dict):
        if member in value:
            return value[member]
        member_str = str(member)
        if member_str in value:
            return value[member_str]
        return None
    if isinstance(value, list) and idx < len(value):
        return value[idx]
    if isinstance(value, tuple) and idx < len(value):
        return value[idx]
    return value


def numeric_sum(values: List[Any]) -> Optional[float]:
    nums = [to_float(v) for v in values]
    nums = [v for v in nums if v is not None]
    return sum(nums) if nums else None


def flatten_trace_stats(row: Dict[str, Any]) -> None:
    stats = row.get("trace_stats")
    if not isinstance(stats, dict):
        return
    for src, dst in (
        ("accepted_tokens", "accepted_tokens"),
        ("invalidated_predraft_tokens", "invalidated_predraft_tokens"),
    ):
        if dst not in row and src in stats:
            row[dst] = stats[src]


def aggregate_low_level_traces(
    trace_rows: List[Dict[str, Any]],
    abs_workload: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """Collapse scheduler/iteration event traces into one row per request.

    This is intentionally conservative: it carries timing and token counters that
    are explicit in low-level rows, then merge_traces_with_workload fills missing
    workload/output fields. It avoids falling back to one global elapsed time for
    every request solely because the trace payload is event-level.
    """
    workload_ids = {str(req["request_id"]) for req in abs_workload}
    seq_to_request: Dict[str, str] = {}

    groups: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    for event in trace_rows:
        req_ids = event.get("request_ids")
        seq_ids = event.get("scheduled_seq_ids")

        members: List[Tuple[str, Any]] = []
        if isinstance(req_ids, list) and req_ids:
            members = [("request_id", req_id) for req_id in req_ids]
            if isinstance(seq_ids, list):
                for seq_id, req_id in zip(seq_ids, req_ids):
                    seq_to_request[str(seq_id)] = str(req_id)
        elif isinstance(seq_ids, list) and seq_ids:
            members = [("seq_id", seq_id) for seq_id in seq_ids]
        else:
            direct_id = row_request_or_seq_id(event)
            if direct_id is not None:
                key_name = "request_id" if str(direct_id) in workload_ids else "seq_id"
                members = [(key_name, direct_id)]

        for idx, (key_name, member_id) in enumerate(members):
            group_key = str(member_id)
            if key_name == "seq_id" and group_key in seq_to_request:
                group_key = seq_to_request[group_key]
            member_event = dict(event)
            member_event[key_name] = member_id
            groups[group_key].append({"event": member_event, "member": member_id, "member_idx": idx})

    aggregated: List[Dict[str, Any]] = []
    for group_key, entries in groups.items():
        events = [entry["event"] for entry in entries]
        row: Dict[str, Any] = {}
        if group_key in workload_ids:
            row["request_id"] = group_key
        else:
            row["seq_id"] = group_key

        starts = [
            to_float(first_present(e, ["decode_start_ts", "verify_start_ts", "draft_start_ts", "total_iteration_start_ts", "start_ts"]))
            for e in events
        ]
        ends = [
            to_float(first_present(e, ["finish_ts", "verify_end_ts", "draft_end_ts", "total_iteration_end_ts", "end_ts"]))
            for e in events
        ]
        starts = [x for x in starts if x is not None]
        ends = [x for x in ends if x is not None]
        if starts:
            row["decode_start_ts"] = min(starts)
        if ends:
            row["finish_ts"] = max(ends)
        if starts and ends and max(ends) >= min(starts):
            row["decode_elapsed_ms"] = (max(ends) - min(starts)) * 1000.0

        for key in ("execution_mode", "decode_ready_mode"):
            for e in events:
                if key in e:
                    row[key] = e[key]
                    break

        accepted: List[Any] = []
        invalidated: List[Any] = []
        for entry in entries:
            e = entry["event"]
            member = entry["member"]
            idx = entry["member_idx"]
            accepted.append(value_for_member(first_present(e, ["accepted_tokens_per_seq", "per_seq_accepted_len", "accepted_tokens"]), idx, member))
            invalidated.append(value_for_member(first_present(e, ["per_seq_invalidated_predraft_len", "invalidated_predraft_tokens"]), idx, member))
        accepted_sum = numeric_sum(accepted)
        invalidated_sum = numeric_sum(invalidated)
        if accepted_sum is not None:
            row["accepted_tokens"] = int(accepted_sum)
        if invalidated_sum is not None:
            row["invalidated_predraft_tokens"] = int(invalidated_sum)

        row["num_low_level_events"] = len(events)
        aggregated.append(row)

    if aggregated:
        print(
            f"[INFO] Aggregated {len(trace_rows)} low-level trace rows "
            f"into {len(aggregated)} request/sequence rows."
        )
    return aggregated


def select_request_level_traces(
    trace_rows: List[Dict[str, Any]],
    abs_workload: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """
    engine.get_traces() may return request summaries or low-level event traces.
    Return one request-level row per request whenever possible.

    Rules:
    1. If rows look like request-level summaries with matching ids, keep them.
    2. If non-vector row count equals workload size, trust positional alignment.
    3. Otherwise aggregate low-level event rows by request_id/seq_id.
    4. If nothing can be mapped, let merge_traces_with_workload synthesize rows.
    """
    if not trace_rows:
        return []

    workload_ids = {str(req["request_id"]) for req in abs_workload}
    seq_ids = {str(req["seq_id"]) for req in abs_workload if "seq_id" in req}

    matched = []
    for row in trace_rows:
        flatten_trace_stats(row)
        if is_request_level_trace_row(row, workload_ids, seq_ids):
            matched.append(row)

    if matched:
        if len(matched) != len(abs_workload):
            print(
                f"[WARN] Matched {len(matched)} request-level traces, "
                f"but workload chunk has {len(abs_workload)} requests."
            )
        return matched

    has_vector_low_level_rows = any(
        isinstance(row.get(key), list)
        for row in trace_rows
        for key in ("request_ids", "scheduled_seq_ids")
    )
    if len(trace_rows) == len(abs_workload) and not has_vector_low_level_rows:
        return trace_rows

    aggregated = aggregate_low_level_traces(trace_rows, abs_workload)
    if aggregated:
        filtered = [
            row
            for row in aggregated
            if row.get("request_id") is None or str(row.get("request_id")) in workload_ids
        ]
        if filtered:
            return filtered
        return aggregated

    print(
        f"[WARN] Could not map {len(trace_rows)} engine trace rows "
        f"to {len(abs_workload)} workload requests; synthesizing request rows."
    )
    return []


def request_level_traces_from_payload(
    trace_payload: Any,
    abs_workload: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    high_level = extract_high_level_trace_list(trace_payload)
    if high_level:
        return select_request_level_traces(high_level, abs_workload)
    return select_request_level_traces(list(iter_low_level_trace_rows(trace_payload)), abs_workload)


def first_present(row: Dict[str, Any], keys: List[str]) -> Any:
    for key in keys:
        if key in row and row[key] is not None:
            return row[key]
    return None


def to_float(value: Any, default: Optional[float] = None) -> Optional[float]:
    if value is None:
        return default
    try:
        return float(value)
    except Exception:
        return default


def to_int(value: Any, default: Optional[int] = None) -> Optional[int]:
    if value is None:
        return default
    try:
        return int(value)
    except Exception:
        return default


def infer_num_output_tokens(
    row: Dict[str, Any],
    fallback: Optional[int] = None,
) -> int:
    value = first_present(
        row,
        [
            "num_decode_output_tokens",
            "num_output_tokens",
            "num_completion_tokens",
            "completion_tokens",
            "output_tokens",
            "num_generated_tokens",
            "num_tokens",
            "generated_tokens",
        ],
    )

    if isinstance(value, int):
        return value

    if isinstance(value, float):
        return int(value)

    if isinstance(value, list):
        return len(value)

    value_int = to_int(value)
    if value_int is not None:
        return value_int

    token_ids = first_present(row, ["token_ids", "output_token_ids", "completion_token_ids"])
    if isinstance(token_ids, list):
        return len(token_ids)

    if fallback is not None:
        return int(fallback)

    return 0


def infer_decode_elapsed_ms(
    row: Dict[str, Any],
    num_output_tokens: int,
    fallback_engine_elapsed_s: Optional[float] = None,
) -> Optional[float]:
    direct_ms = first_present(
        row,
        [
            "decode_elapsed_ms",
            "decoding_elapsed_ms",
            "generation_elapsed_ms",
            "elapsed_ms",
        ],
    )
    direct_ms = to_float(direct_ms)
    if direct_ms is not None:
        return direct_ms

    direct_s = first_present(
        row,
        [
            "decode_elapsed_s",
            "decoding_elapsed_s",
            "generation_elapsed_s",
            "elapsed_s",
            "elapsed_time",
        ],
    )
    direct_s = to_float(direct_s)
    if direct_s is not None:
        return direct_s * 1000.0

    finish_ts = to_float(
        first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"])
    )
    decode_start_ts = to_float(
        first_present(
            row,
            [
                "decode_start_ts",
                "decoding_start_ts",
                "first_decode_ts",
                "first_token_ts",
                "start_decode_ts",
            ],
        )
    )

    if finish_ts is not None and decode_start_ts is not None and finish_ts >= decode_start_ts:
        return (finish_ts - decode_start_ts) * 1000.0

    start_ts = to_float(first_present(row, ["start_ts", "start_time", "arrival_ts"]))
    if finish_ts is not None and start_ts is not None and finish_ts >= start_ts:
        return (finish_ts - start_ts) * 1000.0

    if fallback_engine_elapsed_s is not None and num_output_tokens > 0:
        # Last-resort fallback. This is only a rough approximation for old traces.
        return fallback_engine_elapsed_s * 1000.0

    return None


def infer_observed_tpot_ms(
    row: Dict[str, Any],
    num_output_tokens: int,
    fallback_engine_elapsed_s: Optional[float] = None,
) -> Optional[float]:
    direct_tpot = first_present(
        row,
        [
            "observed_tpot_ms",
            "tpot_ms",
            "avg_tpot_ms",
            "mean_tpot_ms",
        ],
    )
    direct_tpot = to_float(direct_tpot)
    if direct_tpot is not None:
        return direct_tpot

    if num_output_tokens <= 0:
        return None

    decode_elapsed_ms = infer_decode_elapsed_ms(
        row,
        num_output_tokens=num_output_tokens,
        fallback_engine_elapsed_s=fallback_engine_elapsed_s,
    )

    if decode_elapsed_ms is None:
        return None

    return decode_elapsed_ms / max(num_output_tokens, 1)


def build_abs_workload(
    workload: List[Dict[str, Any]],
    workload_start_ts: float,
    use_absolute_arrival_ts: bool,
    replay_arrivals: bool,
) -> List[Dict[str, Any]]:
    abs_workload: List[Dict[str, Any]] = []

    for req in workload:
        row = dict(req)
        row["arrival_ts"] = materialize_arrival_ts(
            req,
            workload_start_ts=workload_start_ts,
            use_absolute_arrival_ts=use_absolute_arrival_ts,
            replay_arrivals=replay_arrivals,
        )
        abs_workload.append(row)

    return abs_workload


def merge_traces_with_workload(
    trace_rows: List[Dict[str, Any]],
    abs_workload: List[Dict[str, Any]],
    output_num_tokens: List[int],
    output_num_acc_tokens: Any,
    run_start_ts: float,
    run_end_ts: float,
    engine_elapsed_s: float,
) -> List[Dict[str, Any]]:
    """
    Merge engine traces with workload metadata.

    Priority:
    1. Match by request_id.
    2. Fallback to output order / workload order.
    3. If no engine traces, synthesize minimal trace rows.
    """
    workload_by_id = {
        str(req["request_id"]): req
        for req in abs_workload
        if "request_id" in req
    }

    merged: List[Dict[str, Any]] = []

    if trace_rows:
        for idx, trace in enumerate(trace_rows):
            flatten_trace_stats(trace)
            req_id = first_present(
                trace,
                [
                    "request_id",
                    "external_request_id",
                    "req_id",
                    "id",
                ],
            )

            base: Dict[str, Any] = {}

            if req_id is not None and str(req_id) in workload_by_id:
                base.update(workload_by_id[str(req_id)])
            elif idx < len(abs_workload):
                base.update(abs_workload[idx])

            base.update(trace)

            if "request_id" not in base and idx < len(abs_workload):
                base["request_id"] = abs_workload[idx]["request_id"]

            if "arrival_ts" not in base and idx < len(abs_workload):
                base["arrival_ts"] = abs_workload[idx]["arrival_ts"]

            if "num_output_tokens" not in base and idx < len(output_num_tokens):
                base["num_output_tokens"] = output_num_tokens[idx]

            if "num_output_tokens" not in base and "num_decode_output_tokens" in base:
                base["num_output_tokens"] = base["num_decode_output_tokens"]

            if "num_acc_tokens" not in base:
                try:
                    base["num_acc_tokens"] = output_num_acc_tokens[idx]
                except Exception:
                    pass

            merged.append(base)

        return merged

    # No engine traces: synthesize minimal traces from outputs.
    for idx, req in enumerate(abs_workload):
        row = dict(req)

        if idx < len(output_num_tokens):
            row["num_output_tokens"] = int(output_num_tokens[idx])
        else:
            row["num_output_tokens"] = 0

        try:
            row["num_acc_tokens"] = output_num_acc_tokens[idx]
        except Exception:
            pass

        row["run_start_ts"] = run_start_ts
        row["decode_start_ts"] = max(run_start_ts, run_end_ts - engine_elapsed_s)
        row["finish_ts"] = run_end_ts
        row["decode_elapsed_ms"] = engine_elapsed_s * 1000.0

        merged.append(row)

    return merged




def normalize_request_timing(
    row: Dict[str, Any],
    num_output_tokens: int,
    decode_ready: bool,
) -> Dict[str, Any]:
    """Normalize request-level decode timing fields.

    In decode-ready evaluation, prefill is intentionally outside the measured
    interval. If wall-clock decode start and finish timestamps are available,
    they are the source of truth for request-level elapsed time and TPOT.
    """
    finish_ts = to_float(
        first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"])
    )
    decode_start_ts = to_float(
        first_present(
            row,
            [
                "decode_start_ts",
                "decoding_start_ts",
                "first_decode_ts",
                "first_token_ts",
                "start_decode_ts",
            ],
        )
    )

    if decode_ready and finish_ts is not None and decode_start_ts is not None:
        if finish_ts >= decode_start_ts:
            decode_elapsed_ms = (finish_ts - decode_start_ts) * 1000.0
            row["decode_elapsed_ms"] = decode_elapsed_ms
            row["observed_tpot_ms"] = (
                decode_elapsed_ms / num_output_tokens if num_output_tokens > 0 else None
            )
        else:
            row["decode_elapsed_ms"] = None
            row["observed_tpot_ms"] = None

    return row


def validate_timing_invariants(rows: List[Dict[str, Any]], decode_ready: bool) -> None:
    arrival_after_finish = 0
    missing_decode_elapsed = 0
    finish_before_decode_start = 0
    missing_observed_tpot = 0
    elapsed_mismatch = 0
    tpot_mismatch = 0
    suspicious_short = 0
    tol_ms = 1e-3

    for row in rows:
        arrival_ts = to_float(row.get("arrival_ts"))
        finish_ts = to_float(
            first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"])
        )
        decode_start_ts = to_float(
            first_present(
                row,
                [
                    "decode_start_ts",
                    "decoding_start_ts",
                    "first_decode_ts",
                    "first_token_ts",
                    "start_decode_ts",
                ],
            )
        )
        decode_elapsed_ms = to_float(row.get("decode_elapsed_ms"))
        observed_tpot_ms = to_float(row.get("observed_tpot_ms"))
        num_output_tokens = infer_num_output_tokens(row)

        if arrival_ts is not None and finish_ts is not None and arrival_ts > finish_ts:
            arrival_after_finish += 1
        if decode_ready and decode_elapsed_ms is None:
            missing_decode_elapsed += 1
        if finish_ts is not None and decode_start_ts is not None:
            if finish_ts < decode_start_ts:
                finish_before_decode_start += 1
            else:
                expected_elapsed_ms = (finish_ts - decode_start_ts) * 1000.0
                if (
                    decode_elapsed_ms is not None
                    and abs(decode_elapsed_ms - expected_elapsed_ms) > tol_ms
                ):
                    elapsed_mismatch += 1
        if num_output_tokens > 0 and observed_tpot_ms is None:
            missing_observed_tpot += 1
        if (
            num_output_tokens > 0
            and decode_elapsed_ms is not None
            and observed_tpot_ms is not None
        ):
            expected_tpot_ms = decode_elapsed_ms / num_output_tokens
            if abs(observed_tpot_ms - expected_tpot_ms) > tol_ms:
                tpot_mismatch += 1
        if (
            decode_ready
            and num_output_tokens > 200
            and decode_elapsed_ms is not None
            and decode_elapsed_ms < 1000
        ):
            suspicious_short += 1

    warnings = [
        (arrival_after_finish, "row(s) have arrival_ts > finish_ts"),
        (missing_decode_elapsed, "decode-ready row(s) are missing decode_elapsed_ms"),
        (finish_before_decode_start, "row(s) have finish_ts < decode_start_ts"),
        (missing_observed_tpot, "row(s) with output tokens are missing observed_tpot_ms"),
        (
            elapsed_mismatch,
            "row(s) have decode_elapsed_ms inconsistent with finish_ts - decode_start_ts",
        ),
        (
            tpot_mismatch,
            "row(s) have observed_tpot_ms inconsistent with decode_elapsed_ms / tokens",
        ),
        (
            suspicious_short,
            "decode-ready row(s) generated >200 tokens in <1000ms; "
            "verify wall-clock timestamps",
        ),
    ]
    for count, message in warnings:
        if count:
            print(f"[WARN] Timing invariant: {count} {message}.")

def choose_goodput_denominator_s(
    rows: List[Dict[str, Any]],
    engine_elapsed_s: float,
    mode: str,
    workload_meta: Dict[str, Any],
) -> float:
    if mode == "engine_elapsed":
        return max(engine_elapsed_s, 1e-9)

    if mode == "workload_duration":
        duration = to_float(workload_meta.get("duration_sec"))
        if duration is not None and duration > 0:
            return duration

        offsets = [
            to_float(row.get("arrival_offset_sec"))
            for row in rows
            if to_float(row.get("arrival_offset_sec")) is not None
        ]
        if offsets:
            return max(max(offsets) - min(offsets), 1e-9)

        return max(engine_elapsed_s, 1e-9)

    if mode == "trace_makespan":
        starts = [
            to_float(row.get("arrival_ts"))
            for row in rows
            if to_float(row.get("arrival_ts")) is not None
        ]
        finishes = [
            to_float(first_present(row, ["finish_ts", "finished_ts", "end_ts", "end_time"]))
            for row in rows
        ]
        finishes = [x for x in finishes if x is not None]

        if starts and finishes:
            makespan = max(finishes) - min(starts)
            if makespan > 0:
                return makespan

        return max(engine_elapsed_s, 1e-9)

    raise ValueError(f"Unknown goodput denominator mode: {mode}")


def summarize_group(
    rows: List[Dict[str, Any]],
    denominator_s: float,
) -> Dict[str, Any]:
    if not rows:
        return {
            "num_requests": 0,
            "num_attained": 0,
            "slo_attainment": 0.0,
            "goodput_tokens_per_s": 0.0,
            "mean_tpot_ms": None,
        }

    attained_rows = [row for row in rows if row.get("slo_attained") is True]
    good_tokens = sum(int(row.get("num_output_tokens", 0)) for row in attained_rows)
    tpot_values = [
        float(row["observed_tpot_ms"])
        for row in rows
        if row.get("observed_tpot_ms") is not None
    ]

    return {
        "num_requests": len(rows),
        "num_attained": len(attained_rows),
        "slo_attainment": len(attained_rows) / max(len(rows), 1),
        "goodput_tokens_per_s": good_tokens / max(denominator_s, 1e-9),
        "mean_tpot_ms": sum(tpot_values) / len(tpot_values) if tpot_values else None,
        "total_output_tokens": sum(int(row.get("num_output_tokens", 0)) for row in rows),
        "attained_output_tokens": good_tokens,
    }


def compute_metrics(
    rows: List[Dict[str, Any]],
    engine_elapsed_s: float,
    denominator_mode: str,
    workload_meta: Dict[str, Any],
    decode_ready: bool = False,
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    evaluated_rows: List[Dict[str, Any]] = []

    for row in rows:
        row = dict(row)

        num_output_tokens = infer_num_output_tokens(row)
        row["num_output_tokens"] = int(num_output_tokens)
        row = normalize_request_timing(row, num_output_tokens, decode_ready=decode_ready)
        if row.get("decode_elapsed_ms") is None:
            row["decode_elapsed_ms"] = infer_decode_elapsed_ms(
                row,
                num_output_tokens=num_output_tokens,
                fallback_engine_elapsed_s=engine_elapsed_s,
            )

        observed_tpot_ms = infer_observed_tpot_ms(
            row,
            num_output_tokens=num_output_tokens,
            fallback_engine_elapsed_s=engine_elapsed_s,
        )
        row["observed_tpot_ms"] = observed_tpot_ms
        if (
            decode_ready
            and row.get("decode_elapsed_ms") is not None
            and num_output_tokens > 0
        ):
            observed_tpot_ms = row["decode_elapsed_ms"] / num_output_tokens
            row["observed_tpot_ms"] = observed_tpot_ms

        slo_tpot_ms = to_float(row.get("slo_tpot_ms"))
        row["slo_tpot_ms"] = slo_tpot_ms

        if observed_tpot_ms is None or slo_tpot_ms is None or num_output_tokens <= 0:
            row["slo_attained"] = False
        else:
            row["slo_attained"] = observed_tpot_ms <= slo_tpot_ms

        evaluated_rows.append(row)

    validate_timing_invariants(evaluated_rows, decode_ready=decode_ready)

    denominator_s = choose_goodput_denominator_s(
        evaluated_rows,
        engine_elapsed_s=engine_elapsed_s,
        mode=denominator_mode,
        workload_meta=workload_meta,
    )

    overall = summarize_group(evaluated_rows, denominator_s)

    by_category: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    by_slo_class: Dict[str, List[Dict[str, Any]]] = defaultdict(list)

    for row in evaluated_rows:
        by_category[str(row.get("category", "unknown"))].append(row)
        by_slo_class[str(row.get("slo_class", "unknown"))].append(row)

    metrics = {
        "overall": overall,
        "by_category": {
            key: summarize_group(value, denominator_s)
            for key, value in sorted(by_category.items())
        },
        "by_slo_class": {
            key: summarize_group(value, denominator_s)
            for key, value in sorted(by_slo_class.items())
        },
        "engine_elapsed_s": engine_elapsed_s,
        "goodput_denominator_mode": denominator_mode,
        "goodput_denominator_s": denominator_s,
    }

    return metrics, evaluated_rows


def run_warmup(engine: PEARLEngine, args: argparse.Namespace) -> None:
    if args.warmup_iters <= 0:
        return

    print(f"[INFO] Running warmup: {args.warmup_iters} iteration(s)")

    warmup_params = SamplingParams(
        temperature=0.0,
        max_tokens=args.warmup_max_tokens,
        ignore_eos=False,
    )

    for i in range(args.warmup_iters):
        engine.add_request(f"Warmup request {i}.", copy.deepcopy(warmup_params))
        engine.generate()

    try_clear_traces(engine)
    print("[INFO] Warmup finished")


def maybe_dump_engine_trace(engine: PEARLEngine, path: Optional[str]) -> None:
    if not path:
        return

    if not hasattr(engine, "dump_traces_json"):
        print("[WARN] engine.dump_traces_json() not found; skip engine trace dump")
        return

    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)

    try:
        engine.dump_traces_json(path)
        print(f"[OK] Dumped engine traces to: {path}")
    except Exception as exc:
        print(f"[WARN] engine.dump_traces_json({path}) failed: {exc}")


def run_generation(
    engine: PEARLEngine,
    execution_mode: str,
    decode_ready: bool,
) -> Tuple[List[str], List[int], Any, float]:
    if decode_ready:
        if not hasattr(engine, "prepare_decode_ready") or not hasattr(engine, "decode_ready_generate"):
            raise RuntimeError(
                "--decode-ready requires engine.prepare_decode_ready() and "
                "engine.decode_ready_generate(...)."
            )
        print("[INFO] Preparing decode-ready requests (prefill is not timed)...")
        engine.prepare_decode_ready()
        print(f"[INFO] Starting decode-ready generation: execution_mode={execution_mode}")
        return engine.decode_ready_generate(execution_mode)

    print(f"[INFO] Starting generation: execution_mode={execution_mode}")
    if execution_mode == "parallel_pearl":
        return engine.generate()
    if execution_mode == "serialized_pearl":
        print(
            "[INFO] Running serialized_pearl approximation baseline; this is not "
            "strict vanilla serial speculative decoding."
        )
        return engine.serialized_pearl_generate()
    if execution_mode == "ar":
        output_text, num_tokens, _, elapsed_time = engine.AR_generate()
        return output_text, num_tokens, None, elapsed_time
    raise ValueError(f"Unknown execution_mode={execution_mode!r}")


def add_workload_chunk(
    engine: PEARLEngine,
    chunk: List[Dict[str, Any]],
    args: argparse.Namespace,
) -> None:
    for idx, req in enumerate(chunk):
        if args.replay_arrivals:
            offset = to_float(req.get("arrival_offset_sec"), 0.0) or 0.0
            first_offset = getattr(args, "_first_arrival_offset", 0.0)
            replay_wall_start = getattr(args, "_replay_wall_start", time.time())
            target_delay = max(offset - first_offset, 0.0)
            now_delay = time.time() - replay_wall_start
            sleep_s = target_delay - now_delay
            if sleep_s > 0:
                time.sleep(sleep_s)

        if not args.use_workload_absolute_arrival_ts:
            # For offline/chunked evaluation, workload offsets are metadata; the
            # request's wall-clock arrival is when we actually enqueue it. When
            # --replay-arrivals is enabled this timestamp is taken after any
            # sleep, so it still reflects the real enqueue time.
            req["arrival_ts"] = time.time()

        sampling_params = make_sampling_params(req, args)
        prompt = get_request_prompt(req)

        add_request_with_metadata(
            engine=engine,
            prompt=prompt,
            sampling_params=sampling_params,
            req=req,
            arrival_ts=float(req["arrival_ts"]),
            fallback_without_metadata=not args.fail_if_add_request_metadata_unsupported,
        )

        if (idx + 1) % 100 == 0:
            print(f"[INFO] Added {idx + 1}/{len(chunk)} requests in current chunk")


def run_eval_chunk(
    engine: PEARLEngine,
    chunk: List[Dict[str, Any]],
    args: argparse.Namespace,
) -> Tuple[List[str], List[int], Any, float, List[Dict[str, Any]]]:
    print(f"[INFO] Adding {len(chunk)} request(s) to engine...")
    add_workload_chunk(engine, chunk, args)

    run_start_ts = time.time()
    output_text, num_tokens, num_acc_tokens, elapsed_time = run_generation(
        engine=engine,
        execution_mode=args.execution_mode,
        decode_ready=args.decode_ready,
    )
    run_end_ts = time.time()
    print(f"[OK] Chunk generation finished. engine_elapsed_s={elapsed_time:.6f}")

    trace_payload = try_get_traces(engine)
    trace_rows = request_level_traces_from_payload(trace_payload, chunk)

    merged_rows = merge_traces_with_workload(
        trace_rows=trace_rows,
        abs_workload=chunk,
        output_num_tokens=num_tokens,
        output_num_acc_tokens=num_acc_tokens,
        run_start_ts=run_start_ts,
        run_end_ts=run_end_ts,
        engine_elapsed_s=elapsed_time,
    )
    for row in merged_rows:
        row.setdefault("execution_mode", args.execution_mode)
        row.setdefault("decode_ready_mode", bool(args.decode_ready))

    return output_text, num_tokens, num_acc_tokens, elapsed_time, merged_rows


def extend_num_acc_tokens(dst: List[Any], src: Any) -> None:
    if src is None:
        return
    try:
        dst.extend(list(src))
    except TypeError:
        dst.append(src)


