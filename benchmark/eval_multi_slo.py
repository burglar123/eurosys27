"""
Evaluate a saved multi-SLO workload on nano-PEARL.

This script reads a workload JSONL generated by gen_multi_slo_workload.py,
feeds requests into PEARLEngine.add_request(...), collects traces, and
computes:
- SLO attainment
- goodput
- mean TPOT
- per-category / per-SLO-class breakdown

Example:

python benchmark/eval_multi_slo.py \
  --draft-model /path/to/draft/model \
  --target-model /path/to/target/model \
  --draft-tp 1 \
  --target-tp 3 \
  --gamma 4 \
  --workload-in benchmark/workloads/rps4.0_mix0.6_0.2_0.2_seed0.jsonl \
  --out results/multislo/rps4.0.json
"""

